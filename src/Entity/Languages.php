<?php

namespace App\Entity;

class Languages
{
    const DEFAULT_LANGUAGE = 1;

    private $id;

    private $title;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getTitle(): ?string
    {
        return $this->title;
    }

    public function setTitle(string $title): self
    {
        $this->title = $title;

        return $this;
    }
}
